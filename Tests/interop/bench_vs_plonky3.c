int main(void) {
    printf("╔══════════════════════════════════════════════════════════════╗\n");
    printf("║  AMO-Lean vs Plonky3: BabyBear Field Arithmetic Benchmark  ║\n");
    printf("╠══════════════════════════════════════════════════════════════╣\n");
    printf("║  Field:      BabyBear (p = 2^31 - 2^27 + 1 = 2013265921)  ║\n");
    printf("║  AMO-Lean:   Solinas fold (verified in Lean 4)             ║\n");
    printf("║  Plonky3:    Montgomery REDC (monty-31/src/utils.rs)       ║\n");
    printf("║  Iterations: %d                                   ║\n", ITERATIONS);
    printf("╚══════════════════════════════════════════════════════════════╝\n\n");

    verify_correctness();

    /* ─── Benchmark 1: Modular Reduction ─── */
    printf("══ Benchmark 1: Modular Reduction (after multiply) ══\n");
    printf("   Input: uint64_t product of two field elements\n");
    printf("   Output: reduced result in [0, ~2p)\n\n");

    BenchResult r_p3   = benchmark_reduce32("Plonky3 Montgomery REDC", plonky3_monty_reduce);
    BenchResult r_amo  = benchmark_reduce("AMO-Lean Solinas fold", amolean_reduce);
    BenchResult r_ss   = benchmark_reduce("AMO-Lean shift-sub (RISC-V)", amolean_reduce_shiftsub);
    printf("\n");

    /* ─── Benchmark 2: Field Multiplication ─── */
    printf("══ Benchmark 2: Field Multiplication (a * b mod p) ══\n");
    printf("   Note: Plonky3 works in Montgomery domain (different repr)\n\n");

    BenchResult m_p3  = benchmark_mul32("Plonky3 monty_mul", plonky3_mul);
    BenchResult m_amo = benchmark_mul64("AMO-Lean reduce(a*b)", amolean_mul);
    printf("\n");

    /* ─── Benchmark 3: NTT Butterfly ─── */
    printf("══ Benchmark 3: NTT Butterfly (a' = a+wb, b' = a-wb mod p) ══\n\n");

    BenchResult bf_p3  = benchmark_butterfly32("Plonky3 Montgomery bf", plonky3_butterfly_correct);
    BenchResult bf_amo = benchmark_butterfly64("AMO-Lean Solinas bf", amolean_butterfly);
    BenchResult bf_hv  = benchmark_butterfly64("AMO-Lean Harvey lazy bf", amolean_butterfly_harvey);
    printf("\n");

    /* ─── Summary ─── */
    printf("╔══════════════════════════════════════════════════════════════╗\n");
    printf("║                      COMPARISON SUMMARY                    ║\n");
    printf("╠══════════════════════════════════════════════════════════════╣\n");
    printf("║  Baseline: Plonky3 Montgomery REDC                        ║\n");
    printf("╚══════════════════════════════════════════════════════════════╝\n\n");

    print_comparison("Reduction: AMO-Lean fold vs Plonky3 REDC", r_p3, r_amo);
    print_comparison("Reduction: AMO-Lean shift-sub vs Plonky3 REDC", r_p3, r_ss);
    print_comparison("Multiply: AMO-Lean vs Plonky3", m_p3, m_amo);
    print_comparison("Butterfly: AMO-Lean Solinas vs Plonky3", bf_p3, bf_amo);
    print_comparison("Butterfly: AMO-Lean Harvey vs Plonky3", bf_p3, bf_hv);

    printf("\n");
    printf("Notes:\n");
    printf("  - Plonky3 uses u32 (Montgomery domain), AMO-Lean uses u64 (natural domain)\n");
    printf("  - Montgomery has a fixed overhead for domain conversion (to_monty/from_monty)\n");
    printf("  - AMO-Lean Solinas fold is proven correct: solinasFold_mod_correct\n");
    printf("  - AMO-Lean Harvey lazy butterfly: harveyBoundRule (output < 2p)\n");
    printf("  - These are SCALAR benchmarks; Plonky3 also has SIMD (AVX2/NEON) variants\n");

    return 0;
}
